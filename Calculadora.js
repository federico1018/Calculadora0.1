/*Creamos una clase que va a poseer todos los metodos/operaciones, las cuatro comunes 
de una calculadora las cuales son suma... etc.*/
class Calculadora {
  suma(num1, num2) {
    return num1 + num2;
  }

  resta(num1, num2) {
    return num1 - num2;
  }

  multiplicacion(num1, num2) {
    return num1 * num2;
  }

  division(num1, num2) {
    return num1 / num2;
  }
}
